void oTerrain::generate(){
    //load for debugging
    VAO* cubeVAO = new VAO();
    OBJLoader::loadOBJ(cubeVAO, "../Assets/cube.obj");
    
    points = 
        std::vector<glm::vec4>((dimensions.x+1)*(dimensions.y+1)*(dimensions.z+1),glm::vec4(0.0));
    for(int x = 0; x <= dimensions.x; x++){
        for(int y = 0; y <= dimensions.y; y++){
            for(int z = 0; z <= dimensions.z; z++){
                float xPos = x*resolution;
                float yPos = y*resolution;
                float zPos = z*resolution;
                float v = generator(glm::vec3(xPos, yPos, zPos));
                points[indexFromVec3(x, y, z)] = glm::vec4(xPos, yPos, zPos, v); 
            } 
        }
    }
}

