float oTerrain::lerp(float a, float b, float p){
    return a * (1 - p) + b * p;
}
    
void oTerrain::march(){
    //generate a bunch of triangles from generator data    
    std::vector<glm::vec3> positions;
    std::vector<glm::vec3> normals;
    std::vector<glm::vec3> colors;
    
    //generate the triangles here
    int count = 0;
    for(int x = 0; x < dimensions.x; x++){
        for(int y = 0; y < dimensions.y; y++){
            for(int z = 0; z < dimensions.z; z++){
                glm::vec4 cubeCorners[8] = {
                    points[indexFromVec3(x  , y  , z  )],  
                    points[indexFromVec3(x+1, y  , z  )],  
                    points[indexFromVec3(x+1, y  , z+1)],  
                    points[indexFromVec3(x  , y  , z+1)],  
                    points[indexFromVec3(x  , y+1, z  )],  
                    points[indexFromVec3(x+1, y+1, z  )],  
                    points[indexFromVec3(x+1, y+1, z+1)],  
                    points[indexFromVec3(x  , y+1, z+1)],  
                };
                
                int cubeIndex = 0;
                if (cubeCorners[0].w < isoLevel) cubeIndex |= 1;
                if (cubeCorners[1].w < isoLevel) cubeIndex |= 2;
                if (cubeCorners[2].w < isoLevel) cubeIndex |= 4;
                if (cubeCorners[3].w < isoLevel) cubeIndex |= 8;
                if (cubeCorners[4].w < isoLevel) cubeIndex |= 16;
                if (cubeCorners[5].w < isoLevel) cubeIndex |= 32;
                if (cubeCorners[6].w < isoLevel) cubeIndex |= 64;
                if (cubeCorners[7].w < isoLevel) cubeIndex |= 128;
                
                std::vector<int> data; 
                data.assign(triangulation[cubeIndex], triangulation[cubeIndex]+16);
                for(int i = 0; data[i] >= 0; i += 3){
                    count++;
                    int a0 = cornerIndexAFromEdge[data[i]];
                    int b0 = cornerIndexBFromEdge[data[i]];

                    int a1 = cornerIndexAFromEdge[data[i+1]];
                    int b1 = cornerIndexBFromEdge[data[i+1]];

                    int a2 = cornerIndexAFromEdge[data[i+2]];
                    int b2 = cornerIndexBFromEdge[data[i+2]];
                    
                    glm::vec3 v0 = interp(cubeCorners[a0], cubeCorners[b0]);
                    glm::vec3 v1 = interp(cubeCorners[a1], cubeCorners[b1]);
                    glm::vec3 v2 = interp(cubeCorners[a2], cubeCorners[b2]);
                   
                    glm::vec3 n0 = makeNormal(v0);
                    glm::vec3 n1 = makeNormal(v1);
                    glm::vec3 n2 = makeNormal(v2);
                    
                    //glm::vec3 n0 = glm::normalize(glm::cross((v2 - v0), (v1 - v0)));

                    positions.push_back(v2);
                    positions.push_back(v1);
                    positions.push_back(v0);
                    normals.push_back(n2);
                    normals.push_back(n1);
                    normals.push_back(n0);
                    colors.push_back(n2);
                    colors.push_back(n1);
                    colors.push_back(n0);
                }
            }  
        }
    }
    
    std::cout << count << std::endl;
    if(positions.size() != 0){
	    glm::vec3 last = positions[positions.size() - 1];
	    std::cout << "last: x: " << last.x << " y: " << last.y << " z: " << last.z << std::endl;
    }
    //add the triangles to the float buffer
    mesh->addFloatBuffer(0, glm::value_ptr(positions[0]), positions.size(), 3); 
    mesh->addFloatBuffer(1, glm::value_ptr(  normals[0]),   normals.size(), 3);
    mesh->addFloatBuffer(2, glm::value_ptr(   colors[0]),    colors.size(), 3);
}

int oTerrain::indexFromVec3(int x, int y, int z){
    return x + ((dimensions.x+1) * y) + ((dimensions.x+1) * (dimensions.y+1) * z);
}

glm::vec3 oTerrain::interp(glm::vec4 a, glm::vec4 b){
    glm::vec3 a3(a);
    glm::vec3 b3(b);
    
    float t = (isoLevel - a.w) / (b.w - a.w);
    glm::vec3 out = a3 + t * (b3-a3);
    return out;
}

glm::vec3 oTerrain::makeNormal(glm::vec3 uvw){
    float d = resolution;
    glm::vec3 grad;
    grad.x = generator(uvw + glm::vec3( d, 0, 0)) -
             generator(uvw + glm::vec3(-d, 0, 0));
    grad.y = generator(uvw + glm::vec3( 0, d, 0)) -
             generator(uvw + glm::vec3( 0,-d, 0));
    grad.z = generator(uvw + glm::vec3( 0, 0, d)) -
             generator(uvw + glm::vec3( 0, 0,-d));
    return -normalize(grad);
}

